# This is the comment in pyton
# Comments can be used to explain Python code.
# Comments can be used to make the code more readable.
# Comments can be used to prevent execution when testing code.
# Comments starts with a #, and Python will ignore them:
print("Hello, World!")  # This is a comment
# print("Hello, World!")
print("Cheers, Mate!")

# Multiline Comments:
# Python does not really have a syntax for multiline comments.
# To add a multiline comment you could insert a # for each line:
# Example:
# This is a comment
# written in
# more than just one line
print("Hello, World!")

"""
This is a comment
written in
more than just one line
"""
print("Hello, World!")

# Difference between Compiler and Interpreter:
# Compiler:
"""
    A compiler translates code from a high-level programming language 
    (like Python, JavaScript or Go) into machine code before the program runs.
"""

# Interpreter:
"""
    An interpreter translates code written in a high-level programming language 
    into machine code line-by-line as the code runs.
"""
